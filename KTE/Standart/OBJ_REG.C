       // В случае необходимости выполнить выбор объектной ОС вместо выбора штатной ОС
       // необходимо раскоментировать dh .
       // dh = 1 ;

       break;
         //-------------

            // Задание на ток якоря.
    case _Obj_Id_Zad :
       break;
         //-------------

            // Ограничение тока.
    case _Obj_Id_Zad_Ogr :
