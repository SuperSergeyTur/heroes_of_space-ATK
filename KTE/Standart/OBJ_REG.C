         //-------------

            // Задание на скорость после ЗИ.
    case _Obj_N_Zad_R :

       // В случае необходимости , проверить полученное Объектное Задание
       // Скорости на превышение ограничений ( раскоментировать dh ) .
       // dh = 1 ;

       break;
         //-------------

            // Дополнительная ОС по скорости.
