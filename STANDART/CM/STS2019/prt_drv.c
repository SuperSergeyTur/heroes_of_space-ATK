        //  Все разрозненные сигналы собираем в один порт.
void  Port_output ( void )
{
  byte ax;
  Po0_nm.all = Po0_c.all ^ _r.Po0_Normal ; // Нормализация выходных сигналов .
  ax = (~Po0_nm.all)&0x3;
  LPC_GPIO1->CLR = ax;
  
  ax = Po0_nm.all&0x3;
  LPC_GPIO1->SET = ax;
  
  if (Po0_nm._.n2)
  {
    LPC_GPIO1->SET = 0x1<<4;
  }
  else
  {
    LPC_GPIO1->CLR = 0x1<<4;    
  }

  if (Po0_nm._.n3)
  {
    LPC_GPIO0->SET = 0x1<<15;
  }
  else
  {
    LPC_GPIO0->CLR = 0x1<<15;    
  }
  if (Po0_nm._.n4)
  {
    LPC_GPIO0->SET = 0x1<<16;
  }
  else
  {
    LPC_GPIO0->CLR = 0x1<<16;    
  }
  //LPC_GPIO2->PIN = ((lw)(Po0_nm.all & 0x3F)) << 24 ;
  

  return  ;
}

      //  Для программ стартовой очистки выходных портов.
void  Port_output_clr ( void )
{
    Po0_c.all = 0 ;
    Port_output () ;
  return ;
}
