        //  Все разрозненные сигналы собираем в один порт.
byte read_Pi0 ( void )
{
  register byte al ;
          //
          //           ЪДДДДДДДДДДДДДДДДДДДДДДДP27...P24
          //           і         ЪДДДДДДДДДДДДДP15
          //           і         і       ЪДДДДДP62...P60
          //     ЪДДДДДБДДДДДї   і   ЪДДДБДДДї
          //   ЪДБДВДДДВДДДВДБДТДБДВДБДВДДДВДБДї
          //   і 7 і 6 і 5 і 4 є 3 і 2 і 1 і 0 і
          //   АДДДБДДДБДДДБДДДРДДДБДДДБДДДБДДДЩ

          //   P27...P24              P15             P62...P60
        al = (PDR2 & 0xF0) | ((PDR1 & 0x20) >> 2) | (PDR6 & 0x07) ;

  return al ;
}

