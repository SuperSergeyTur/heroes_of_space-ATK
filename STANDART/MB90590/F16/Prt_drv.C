        //  Все разрозненные сигналы собираем в один порт.
void  Port_output ( void )
{
          //     ЪДДДДДДДДДДДДДДДДДДДДДДДДДДДДДP37
          //     і   ЪДДДДДДДДДДДДДДДДДДДДДДДДДP12
          //     і   і   ЪДДДДДДДДДДДДДДДДДДДДДP35
          //     і   і   і   ЪДДДДДДДДДДДДДДДДДP51
          //     і   і   і   і   ЪДДДДДДДДДДДВДP33...P30
          //   ЪДБДВДБДВДБДВДБДТДБДВДДДВДДДВДБДї
          //   і 7 і 6 і 5 і 4 є 3 і 2 і 1 і 0 і
          //   АДДДБДДДБДДДБДДДРДДДБДДДБДДДБДДДЩ

#ifndef _Po0_INVERT      // Имеется возможность инверсии выходных сигналов .
  Po0_nm.all = Po0_c.all ; // Заглушка при отсуствии инвертирования .
#else
  Po0_nm.all = Po0_c.all ^ _r.Po0_Normal ; // Нормализация выходных сигналов .
#endif
          // P37,P35,P33...P30
  PDR3 = Po0_nm.all & 0xAF ;

  if( (Po0_nm.all & 0x40) == 0 )  PDR1_P12 = 0 ;
  else                            PDR1_P12 = 1 ;

#ifndef _Vozb_KTE   // в КТЭ со встроенным возбудителем этот выход
  if( (Po0_nm.all & 0x10) == 0 )  PDR5_P51 = 0 ; // используется для частотного
  else                            PDR5_P51 = 1 ; // заполнения.
#endif

 /*ДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДД
          //             ЪДДДДДДДДДДДДДДДДДДДДДP13
          //             і   ЪДДДДДДДДДДДДДДДДДP56
          //             і   і   ЪДДДДДДДДДДДВДP23...P20
          //   ЪДДДВДДДВДБДВДБДТДБДВДДДВДДДВДБДї
          //   і 7 і 6 і 5 і 4 є 3 і 2 і 1 і 0 і
          //   АДДДБДДДБДДДБДДДРДДДБДДДБДДДБДДДЩ

  Po1_nm.all = Po1_c.all ^ _r.Po1_Normal ; // Нормализация выходных сигналов .
          // P23...P20
  PDR2 = Po1_nm.all & 0x0F ;

  if( (Po1_nm.all & 0x10) == 0 )  PDR5_P56 = 0 ;
  else                            PDR5_P56 = 1 ;

  if( (Po1_nm.all & 0x20) == 0 )  PDR1_P13 = 0 ;
  else                            PDR1_P13 = 1 ;

    ДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДДД*/

  return  ;
}

      //  Для программ стартовой очистки выходных портов.
void  Port_output_clr ( void )
{
    Po0_c.all = 0 ;
    Po1_c.all = 0 ;
    Port_output () ;
  return ;
}
