        //  Все разрозненные сигналы собираем в один порт.
byte read_Pi1 ( void )
{
  register byte al ;
          //     ЪДДДДДДДДДДДДДДДДДДДДДДДДДДДДДP92
          //     і   ЪДДДДДДДДДДДДДДДДДДДДДДДДДP86
          //     і   і   ЪДДДДДДДДДДДДДДДДДДДВДP27...P22
          //   ЪДБДВДБДВДБДВДДДТДДДВДДДВДДДВДБДї
          //   і 7 і 6 і 5 і 4 є 3 і 2 і 1 і 0 і
          //   АДДДБДДДБДДДБДДДРДДДБДДДБДДДБДДДЩ

          //   P27...P22          P86              P92
        al = (PDR2 >> 2) | (PDR8 & 0x40) | ((PDR9 & 0x04)<<5) ;
        //al = (PDR2 & 0x3F) | (PDR8 & 0x40) | ((PDR9 & 0x04)<<5) ;

  return al ;
}


