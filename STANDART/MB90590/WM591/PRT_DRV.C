        //  Все разрозненные сигналы собираем в один порт.
void  Port_output ( void )
{
  byte bh;

  //Выходы - Р20-Р23

  bh = PDR2;

  bh &=0xf0;

  bh |= (Po0_c.all & 0x0F);

  PDR2 = bh;

  return  ;
}

      //  Для программ стартовой очистки выходных портов.
void  Port_output_clr ( void )
{
    Po0_c.all = 0 ;
    Po1_c.all = 0;
    Port_output () ;
  return ;
}

void  PPG_Out_Set (struct PPG_Prt* out)
{
   lword lax;
   word ax;

   ax = *out->val;

   if ((sw)ax > (sw)out->max ) ax = out->max;
   else if ((sw)ax < (sw)0 )   ax = 0;

   lax = (lw)ax * 2000;
   ax = lax /out->max;
   out->low = 1005 ;
   out->high = ax;
   PPG_set( out->num, out->low , out->high );

}
